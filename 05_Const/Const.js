// All About const

// const - It is a variable declaring type. It was introduced in ES6, Popularly knows as Javascript 2015. 

// It should be Initialised and  declared at the same time
const a = "Abdul Rehman";

// Later it cannot be declared as 
a = "Abdul Rehman"
// It cannot be redeclared 
a = "Kalsekar"
 
// You can Not Reinitiate, Not Redeclare variables with const. They are like Constants

console.log(a)

