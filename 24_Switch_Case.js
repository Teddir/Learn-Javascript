// Using Switch Case in JS
// You can create different cases based on different inputs
// Switch Statements are case Sensitive
// let fruit = "Mango";
// Above Snippet will not work as M is Capital

let fruit = "mango";

switch (fruit) {
    // If input is mango below block of code will be executed
    case 'mango':
        console.log("Fruit is Mango");
        break;

    // If input is orange below block of code will be executed
    case 'Orange':
        console.log("Fruit is Orange");
        break;

}


